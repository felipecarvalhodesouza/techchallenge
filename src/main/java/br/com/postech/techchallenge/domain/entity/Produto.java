package br.com.postech.techchallenge.domain.entity;

public class Produto {

	private String id;
	private double valor;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public double getValor() {
		return valor;
	}

	public void setValor(double preco) {
		this.valor = preco;
	}
}
